            <h3>Comitê de Ética e Pesquisa da PUCRS</h3>
            <p>As atividades de pesquisa têm sido ampliadas em quantidade e complexidade nos últimos anos. Isso provoca
                inúmeros
                questionamentos éticos relativos ao processo de planejar, executar, avaliar e divulgar os resultados dos
                projetos de pesquisa que envolvem seres humanos. Da necessidade de adequar práticas de pesquisa a estas
                mudanças, foi criado, em 17 de maio de 1990, o Comitê de Ética em Pesquisa da PUCRS (CEP), o primeiro
                adequado
                às normas e diretrizes brasileiras. Em 12 de dezembro de 2012, a Resolução CNS 466/12 revogou as Resoluções
                CNS
                Nº 196/96, 303/2000 e 404/2008. O novo texto diz: “Toda pesquisa envolvendo seres humanos deverá ser
                submetida à
                apreciação de um Comitê de Ética em Pesquisa”. O item Termos e Definições, define Comitês de Ética em
                Pesquisa
                como colegiados interdisciplinares e independentes, com “munus público”, de caráter consultivo, deliberativo
                e
                educativo, criados para defender os interesses dos sujeitos da pesquisa em sua integridade e dignidade e
                para
                contribuir no desenvolvimento da pesquisa dentro de padrões eticamente corretos. A partir de 2016, a
                Resolução
                CNS 510/16 estabeleceu que os projetos desenvolvidos na área das Ciências Humanas e Sociais também deverão
                receber apreciação ética pelo Comitê de Ética e Pesquisa. A AVICO é membro do CEP da PUCRS como
                representação da
                sociedade civil (usuários).</p>
            <p>Acesse a página do Comitês através do link: https://www.pucrs.br/pesquisa/comites/cep/</p>
