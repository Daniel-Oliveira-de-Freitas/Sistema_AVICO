                    <p><strong>Garantir recursos do SUS para atendimento da população</strong></p>
                    <p>O presidente do Conselho Nacional de Saúde, Fernando Zasso Pigatto, enfatizou que é preciso insistir
                        com o Congresso Nacional no debate com o Ministério da Saúde, sobre a necessidade de garantir
                        recursos ao SUS para o atendimento da população, pois a pandemia não terminou.</p>
                    <p>A enfermeira Inara Ruas, integrante do Conselho Estadual de Saúde e diretora do Sindicato dos
                        Enfermeiros do Rio Grande do Sul, salientou que, por lei, o governo do estado deveria investir 12%
                        em saúde pública, porém, conforme ela, isso não acontece.</p>
                    <p>Inara lembrou o quanto a Emenda Constitucional 95 tem sido prejudicial à saúde e que, diante disso,
                        existe a necessidade urgente de sua revogação para existir investimentos. Ela também falou sobre a
                        necessidade de identificação do número de vítimas que sofrem com as sequelas da covid-19, bem como
                        das patologias vividas por essas pessoas.</p>
                    <p>A Frente Parlamentar também ouviu vítimas da covid-19, que sofrem com as sequelas da doença. Elas
                        relataram o sofrimento vivenciado durante o período de internação e as dificuldades enfrentadas em
                        conviver com as sequelas e na busca de serviços especializados.</p>
                    <p>Ewelin Canizares, do Movimento Inclusivas, contou que foi internada, teve embolia pulmonar e hoje
                        sofre com perda de memória, fadiga, além de problemas respiratórios. Ela ressaltou que em nenhum
                        momento, durante a pandemia, houve um olhar para as pessoas com deficiência, que, em geral, sofrem
                        com um número maior de comorbidades.</p>
                    <p>O encontro foi uma promoção conjunta entre o Fórum Democrático de Desenvolvimento Regional, por meio
                        do Grupo Executivo de Acompanhamento de Debates (Gead) da Saúde, Educação, Cultura e Envelhecimento
                        Ativo e Saudável, e a Frente Parlamentar em Defesa das Vítimas da Covid-19.</p>
                    <p>A Frente Parlamentar buscará o apoio da Comissão de Saúde e Meio Ambiente da Assembleia Legislativa
                        para a Resolução nº 013/2022, do Conselho Nacional de Saúde, que prevê a adoção pelo Ministério da
                        Saúde de uma linha especial de cuidados para as vítimas da covid-19. Também deverá costurar, entre
                        os deputados estaduais, recursos orçamentários específicos para financiar o tratamento dos pacientes
                        com sequelas.</p>
