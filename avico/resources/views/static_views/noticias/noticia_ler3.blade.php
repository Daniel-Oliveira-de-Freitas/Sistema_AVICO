                <h3>Audiência debate a situação das sequelas das vítimas da covid no Rio Grande do Sul</h3>
                <div class="row pt-3">
                    <img class="img-fluid" src="images\assets\img\noticias\BDF1.png">
                </div>
                <div class="row pt-3">
                    <p>Brasil de Fato | Porto Alegre | 01 de Junho de 2022 às 15:20</p>
                    <p>Com depoimentos de sobreviventes da covid-19, a Frente Parlamentar Defesa das Vítimas da Covid da
                        Assembleia Legislativa do RS realizou, nesta segunda-feira (30), a primeira de sete audiências
                        públicas. Os encontros tem o intuito de debater as sequelas das vítimas da covid-19 e dar
                        visibilidade à questão.</p>
                    <p>No encontro desta segunda, os participantes defenderam a necessidade de mais investimentos do estado
                        no Sistema Único de Saúde (SUS) para garantir atendimento eficaz às vítimas. Na audiência, que
                        também aconteceu sob forma virtual, estiveram presentes mais de 40 entidades representativas e
                        instituições. </p>
                    <p>O deputado estadual Pepe Vargas (PT), que preside a Frente, abriu a audiência destacando a relevância
                        de escutar a população e fazer um mapeamento do impacto da pandemia na vida da população e nos
                        sistemas de saúde e de proteção social das diversas regiões, tendo como foco o atendimento do SUS às
                        vítimas e seus familiares.</p>
                    <p>“Precisamos dar visibilidade às vítimas diretas e indiretas da pandemia e cobrar soluções efetivas do
                        Poder Público para as pessoas que apresentam danos crônicos e permanentes. Além disso, temos que
                        ampliar o olhar para os impactos econômicos e sociais na vida dessas famílias e nas próprias
                        políticas públicas”, ressaltou o deputado.</p>
                    <p>Pepe frisou que existem demandas relacionadas à atenção à saúde da população que apresenta danos
                        crônicos, permanentes ou temporários, causados pela doença, que implicam em novos arranjos
                        assistenciais. Salientou que se soma a este cenário o aumento das “filas” por consultas
                        especializadas, exames diagnósticos, procedimentos ambulatoriais e cirurgias eletivas – represados
                        durante os períodos de restrição dos serviços de saúde.</p>
                    <p>“É preciso dimensionar, construir estratégias urgentes para alterar esse cenário de invisibilidade,
                        desatendimento e sofrimento”, completou. O deputado lembrou ainda que os impactos da pandemia também
                        são econômicos e a Frente Parlamentar também tem como propósito avaliar esses impactos.</p>
                    <p><strong>Governo ignorou sugestões das entidades representativas</strong></p>
                    <p>Vítima das sequelas da covid, o presidente do Conselho Estadual de Saúde (CES), Cláudio Augustin,
                        passou nove meses no hospital entre a vida e a morte. Foi internado com uma pneumonia bacteriana no
                        dia 1º de dezembro de 2020 e, ao pegar covid no ambiente hospitalar, sua vida mudou profundamente.
                        Desde agosto, está em casa, mas ainda na cama. Em sua intervenção, ele acusou o governo do estado de
                        conduzir o setor da saúde “como se nada estivesse acontecendo”.</p>
                    <p>Cláudio destacou que estudos de várias instituições nacionais e internacionais apontam que 70% das
                        pessoas infectadas pelo coronavírus apresentam alguma sequela e 20% apresentam sequelas graves.</p>
                    <p>“Ocorre que de 30% a 50% dos recuperados apresentam sequelas. Se tivemos dois milhões de infectados
                        no Rio Grande do Sul, cerca de 700 mil apresentam danos. A maioria dessas pessoas não conta com
                        qualquer tipo de assistência”, denunciou o presidente. Cláudio frisou que o governo estadual ignorou
                        sugestões feitas pelos Conselhos na adoção de medidas de prevenção, de atendimento à saúde e
                        instituição de protocolos.</p>
                    <p>A proporção de sequelados foi corroborada pela representante do Ministério Público Federal, Ana Paula
                        Carvalho de Medeiros. Ela citou estudo da Fiocruz que aponta um percentual de 50% de sobreviventes
                        com sequelas por mais de um ano e uma lista com 23 sintomas que perduram mesmo após os pacientes
                        serem considerados livres da infecção. </p>
                    <p>A defensora pública da Defensoria Pública Estadual, Liliane Paz Deble, também criticou o estado por
                        deixar de atender sugestões de protocolos e demandas repassadas por entidades representativas, como
                        a falta de leitos. “Quando o cidadão bate na porta da defensoria é porque tudo se agravou e,
                        portanto, que dos nossos trabalhos surjam soluções.”</p>
                    <p><strong>Entidades criticam falta de assistência e orientação nacional </strong></p>
                    <p>Em sua manifestação, a médica Rosângela Dorneles, que é uma das coordenadoras da Associação Vida e
                        Justiça, entidade que atua na busca de reparação para vítimas da pandemia, ressaltou a falta de
                        orientação nacional no enfrentamento da pandemia, que, conforme frisou, ainda não acabou. Criticou a
                        ausência de medidas sanitárias para proteger a população. E alertou também para o “momento
                        extremamente grave” no Rio Grande do Sul com a chegada do inverno e a lotação das UTIs,
                        especialmente, as pediátricas.|</p>
                    <p>“O sistema está fragilizado e fragmentado. Faltam medicações, os trabalhadores estão exaustos e
                        adoecidos e as filas não param de crescer. Estamos em um momento grave na saúde do estado, temos
                        ainda ausência de insumos e a dificuldade é grande para atender. Precisamos de medidas sanitárias
                        firmes para proteger a vida da população, manter a recomendação do uso de máscara e garantir a
                        vacinação de 95% da nossa população”, ressaltou a médica. </p>
                    <p>Por sua vez a fundadora e presidente da Associação de Vítimas e Familiares de Vítimas da Covid-19
                        (Avico Brasil), Paola Falceta, chamou atenção para falta de assistência adequada para órfãos e
                        enlutados. Ela criticou o abandono desse segmento por parte do Poder Público e sustentou que, mesmo
                        depois da “tragédia que foi a pandemia”, o Estado continua sem fazer “ações de prevenção e promoção
                        da saúde, que poderiam ter salvado milhares de vidas”.</p>
                    <p>Também criticou a falta de promoção e prevenção em saúde pelo Estado, o que é um dos princípios do
                        SUS. “Se o Executivo fizesse promoção e prevenção da saúde, com um protocolo nacional, muito dessas
                        mortes não teriam acontecido, O Rio Grande do Sul é exemplo de abandono, precarização e ineficiente
                        em saúde”, afirmou.</p>
                    <p>A representante da Fiocruz Maria Juliana Moura Correa afirmou que, nos próximos dias, a fundação
                        lançará um informe sobre a Covid Longa para ajudar na identificação de alterações e na orientação de
                        tratamentos e reabilitação. </p>
                    <p>De acordo com a pesquisadora a falta de orientações e a necessidade de construção de uma linha de
                        cuidado e de identificação para o tratamento da população que vive com a covid-19 prolongada é uma
                        necessidade urgente. “É preciso incluir a reabilitação física e funcional”, pontuou. </p>
                    <p>Ela também lembrou dos inúmeros trabalhadores na linha de frente, considerados essenciais e que se
                        expuseram sem vacina. “Essas pessoas ficaram sequeladas e há quem não conseguiu retornar ao
                        trabalho”, frisa Maria Juliana.</p>
                    <p>A procuradora da República, Ana Paula Carvalho de Medeiros, apontou o estudo da Fiocruz que informa
                        que metade da população infectada pela covid-19 apresenta sequelas, com duração de, no mínimo, um
                        ano. No entanto, segundo ela, não existe um protocolo nacional de monitoramento e de avaliação
                        desses casos.</p>
