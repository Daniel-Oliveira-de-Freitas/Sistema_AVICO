                <div class="row pt-3">
                    <p>Criar uma Frente Parlamentar em Defesa das Vítimas da Covid-19, realizar uma Audiência Pública e um
                        grande seminário com a participação da Secretaria Estadual da Saúde (SES), sociedade civil e
                        Universidades Federais e, a partir dele, vários encontros regionais para discutir ações que auxiliem
                        na construção de políticas públicas para as pessoas que tratam das sequelas deixadas pela doença.
                        Estes foram os encaminhamentos tirados do encontro realizado na manhã desta quarta-feira (23) entre
                        presidente da Assembleia Legislativa do Rio Grande do Sul, deputado Valdeci Oliveira (PT), e
                        representantes do Comitê Estadual em Defesa das Vítimas de Covid 19, da Associação das Vítimas de
                        Covid-19 (Avico) e da Associação Nacional em Apoio e Defesa dos Direitos das Vítimas da Covid19 –
                        Vida e Justiça. </p>
                    <p>O objetivo é organizar ações coletivas para melhorar a qualidade de vida e dar maior visibilidade às
                        pessoas que ficaram com sequelas em decorrência da doença, pois de acordo com o Conselho Estadual de
                        Saúde (CES), os indicadores só mostram os números de contaminados, óbitos e recuperados da covid-19,
                        mas não mostram os que ficam com sequelas, muitas delas graves. “Precisamos dar visibilidade às
                        vítimas e aos órfãos, mas para isso é preciso a participação do setor público, por isso queremos que
                        seja criada uma Frente Parlamentar em defesa das vítimas”, afirmou a vice-presidente do CES, Inara
                        Beatriz Amaral Ruas.</p>
                    <p>Para o presidente do Parlamento gaúcho, o pedido, uma das ações previstas da gestão, é perfeitamente
                        plausível e necessário. Valdeci sugeriu que o Fórum Democrático articule junto aos mandatos dos
                        deputados Jeferson Fernandes e Pepe Vargas, ambos do PT, a criação da Frente Parlamentar e a
                        realização do Seminário para debater e organizar ações em benefício das vítimas da Covid-19. Este
                        fórum deverá ficar encarregado também de articular e reivindicar junto à SES uma ação imediata que
                        resolva o déficit de atendimentos. “As pessoas não estão conseguindo acessar fisioterapia
                        respiratória, há filas para todos os atendimentos, inclusive para cirurgias, e não há previsão de
                        que as pessoas sejam atendidas. Só dizem que precisamos aguardar”, afirmou a coordenadora do Núcleo
                        de Saúde da Associação Nacional em Apoio e Defesa dos Direitos das Vítimas da Covid-19 – Vida e
                        Justiça, Rosângela Dorneles.</p>
                    <p>Outra reivindicação das associações é para que seja levantada a quantidade de vítimas em tratamento
                        das sequelas. “Temos um grande número de profissionais da saúde com sequelas diferenciadas que vão
                        de leve a graves, mas para o Rio Grande do Sul essas pessoas estão invisíveis. Sabemos que elas
                        existem, mas precisamos que a Assembleia Legislativa e a sociedade saibam delas”, disse a presidente
                        da Avico, Paola Falceta. Segundo ela, a estimativa é de que no Rio Grande do Sul haja mais de 500
                        órfãos, mas não há um levantamento oficial. “Quem tem estes números são os cartórios de registros de
                        nascimentos e óbitos. Por isso há a possibilidade de monitorar, mas precisamos que a Assembleia e o
                        poder público nos auxilie”, apelou.</p>
                    <p>Valdeci afirmou ainda que é possível buscar dados detalhados a partir de parcerias com a Universidade
                        Federal de Pelotas, por exemplo. “Podemos realizar na segunda metade de março um seminário com a
                        participação do professor infectologista Pedro Hallal, que já se colocou à disposição para auxiliar
                        no levantamento de dados mais amplos”, disse, defendendo que haja uma grande mobilização. “Não
                        podemos naturalizar o que está acontecendo. Há interesse de alguns setores em não divulgar as
                        mortes, mas precisamos dar visibilidade às vítimas para que o Estado estabeleça políticas públicas
                        voltadas a elas”. O encontro também contou com a participação de representantes dos mandatos dos
                        deputados Pepe Vargas e Jeferson Fernandes, da assessoria técnica da bancada do PT, além do
                        presidente do Conselho Nacional da Saúde, Fernando Pigatto.</p>
