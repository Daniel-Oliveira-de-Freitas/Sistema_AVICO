                <div class="row pt-3">
                    <p>Agência de nóticias</p>
                    <p>A Assembleia Legislativa instalou, no início da tarde desta quarta-feira (27), a Frente Parlamentar
                        em Defesa das Vítimas da Covid-19, que será presidida pelo deputado Pepe Vargas (PT). O ato em
                        formato híbrido ocorreu no Salão Júlio de Castilhos e contou com a participação de representantes de
                        entidades ligadas à área da saúde locais e nacionais.<br>
                        Instigada pela Associação das Vítimas da Covid,
                        que pediu apoio ao presidente do parlamento gaúcho, Valdeci Oliveira (PT), para a construção de
                        políticas públicas voltadas aos sobreviventes da pandemia, a frente deverá realizar audiências
                        públicas nas cidades polos de todas as macrorregiões de saúde do estado. “O objetivo é promover um
                        amplo debate envolvendo os poderes e a sociedade civil sobre a situação das vítimas e contribuir
                        para a construção de políticas públicas”, explicou Pepe Vargas.
                    </p>
                    <p>
                        O parlamentar alertou ainda que as consequências da Covid-19 não se restringem à saúde, onde são
                        graves, mas que avançam no campo econômico e social, principalmente, nas famílias que perderam
                        provedores para a doença. “A Covid não é democrática e não atinge todas as classes sociais de
                        maneira igual. Pessoas com menor poder aquisitivo foram mais impactadas e isso também será objeto de
                        discussão”, apontou, lembrando que a Reforma da Previdência impôs regras draconianas para concessão
                        de pensões por morte, provocando ainda mais prejuízos para quem teve perdas de familiares por conta
                        da pandemia.</p>
                    <p>
                        O presidente da Assembleia Legislativa contou que enfrentou um caso grave de coronavírus na família
                        e que conhece as dificuldades pelos quais passam pacientes e familiares durante e, muitas vezes,
                        após a alta hospitalar. Valdeci considera que, mesmo com todas as limitações, o Sistema Único de
                        Saúde foi fundamental para que a situação não fosse ainda mais dramática para os brasileiros. E
                        ressaltou que a pandemia não foi totalmente superada, que muitos ainda não concluíram a imunização e
                        que mesmos os vacinados não estão, totalmente, livres da doença. Lamentou também que, mesmo diante
                        de um número expressivo de pessoas com sequelas e da demanda reprimida na saúde, os governos não
                        aplicaram um centavo a mais no setor.
                        Ele garantiu que o parlamento gaúcho dará todo o apoio para que a frente se “espraie pelo Rio Grande
                        do Sul para buscar soluções junto com a sociedade e criar a consciência na população de que saúde
                        não é custo, é investimento”.

                    </p>
                    <p>Entidades</p>
                    <p>A presidente da Associação das Vítimas da Covid, Paola Falceta, relatou casos de pessoas que perderam
                        familiares e de que convivem com sequelas da doença sem receber o tratamento adequado. Segundo ela,
                        a maioria dos sobreviventes recebeu alta hospitalar e nunca mais teve uma consulta pelo SUS.
                        Paola criticou também a falta de dados no estado sobre os sequelados e os órfãos da Covid. “O que
                        vemos é um total desprezo pelas vítimas. Por isso, temos que nos movimentar. Há gente morrendo por
                        fome ou por problemas de saúde, relacionados à Covid-19”, conclamou.</p>
                    <p>Representando o Movimento Luta pela Vida, Lúcia Souto disse que o Brasil teve a “pior gestão da
                        pandemia do mundo” e que agora os governantes tentam colocar a situação dos sobreviventes para baixo
                        do tapete, fazendo de conta que nada está acontecendo. Para ela, a falta de recursos é uma
                        justificativa que não se sustenta tendo em vista gastos em outras áreas nem tão prioritárias.</p>
                    <p>Também participaram da cerimônia o presidente do Conselho Estadual de Saúde, Cláudio Augustin, que se
                        manifestou de forma virtual por estar em casa se recuperando da doença; o presidente do Conselho
                        Nacional de Saúde, Fernando Pigatto; representantes do Conselho de Secretárias Municipais de Saúde,
                        representante da Defensoria Pública, sindicalistas e lideranças políticas de diversas regiões do
                        estado, além dos deputados Jeferson Fernandes e Zé Nunes, ambos do PT. </p>
                    <p>As Audiências Públicas ocorrerão em sete cidades das macrorregiões de saúde do Estado Gaúcho, sendo
                        elas: Porto Alegre, Lajeado, Santa Maria, Pelotas, Passo Fundo, Santa Rosa e Caxias do Sul. Em breve
                        a programação será divulgada nos canais das entidades que compõem a Frente Parlamentar, fique
                        atento!</p>
